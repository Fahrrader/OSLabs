int main(int argn, char *args[]) {
	int n;
	
	if (argn >= 2) {	
		sscanf(args[1], "%i", &n);
	} else {
		n = 0;
	}
	
	for (int i = 1; i <= n; i++) {
		for (int j = 1; j <= n + i - 1; j++) {
			if (j <= n - i)
				printf(" ");
			else
				printf("*");
		}
		printf("\n");
	}
	
	n = -1;
	
	while (n != 0) {
		printf("Enter number of drawing function: ");
		scanf("%i", &n);
		switch (n) {
			case 1:
				printFigure1();
				break;
			case 2:
				printFigure2();
				break;
			case 0:
				printf("Goodbye, then.");
				break;
			default:
				printf("No such function.");
		}
		printf("\n");
	}
	
	return 0;
}